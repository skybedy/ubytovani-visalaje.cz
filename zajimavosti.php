<h2>Běžecké trasy, sjezdovky, zimní sporty</h2>

<p>Tato oblast je vhodným prostředím také pro různé zimní aktivity. V blízkosti našeho ubytování na Visalajích je několik běžeckých tras, takže zdejší hory můžete obdivovat také v zimních měsících.</p>

<p>Pokud máte raději trochu adrenalinu, můžete navštívit některou ze sjezdovek v okolí. V okruhu 15-ti km se nachází asi 5 lyžařských vleků - Visalaje Radegast, Morávka - Sviňorky, Zlatník - Biocel, Zlatník Fela a Sihly Radegast.</p>

