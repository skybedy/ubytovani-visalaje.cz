	<h2 class="top">Zajímavosti v okolí</h2>
	
<p>Naše chalupa se nacházejí na úplném konci obce Morávka ležící v 
Beskydech v severo-východní části Moravskoslezského kraje. Jsme obklopeni krásnou přírodou, 
která je protkaná řadou turistických stezek vedoucích k mnoha zajímavostem a turistických 
lákadel, jež se v této oblasti nacházejí.</p>

<p>Rádi bychom Vás na tato místa pozvali a nabídli Vám pár tipů, kam vyrazit a co zajímavého vidět.</p>

<h3>Lysá hora</h3>

<ul>
	<li>1323 m n. m., Beskydy</li>
	<li>vzdálenost cca 10,5 km od naší chalupy</li>
</ul>

<p>Lysá hora je <strong>nejvyšší vrchol Moravskoslezských Beskyd</strong> a zároveň královna všech slezských hor. 
Leží na rozhraní obcí Krásná, Malenovice, Ostravice a Staré Hamry. Na holém (lysém) vrcholu, 
na který vede silnice z Krásné, se nachází charakteristický telekomunikační vysílač, meteorologická stanice a několik horských chat.</p>

<p>Samotnému vrcholu se přezdívá <strong>Gigula</strong>. Z vrcholu Lysé hory vybíhají čtyři rozsochy: Malchor, 
Zimný (Zimní Polana), Kobylanka (Velký Kobylík) a Lukšinec. Vrchol představuje <strong>významnou křižovatku turistických 
cest</strong> a dvakrát týdně na něj dojíždí autobusový spoj. </p>

<h3>Visalaje</h3>

<ul>
	<li>800 m n. m., Beskydy</li>
	<li>vzdálenost cca 1 km od naší chalupy</li>
</ul>

<p>Vyvýšený konec dvanáctikilometrového horského údolí mezi <strong>Lysou horou</strong> (1324 m n. m.), 
<strong>Travným</strong> (1204 m n. m.) a <strong>Bílým Křížem</strong> (905 m n. m.). Jedná se o velmi oblíbenou turistickou oblast Beskyd lákající celoročně sportovce a milovníky hor, v sezónu pak také např. houbaře. Visalaje jsou vhodným výchozím bodem pro výšlapy do této části Beskyd, je zde také dobré autobusové spojení. </p>

<h3>Bílý kříž</h3>

<ul>
	<li>905 m n. m., Beskydy</li>
	<li>vzdálenost cca 3 km od naší chalupy</li>
</ul>
	
<p>Jméno této velmi známé rekreační oblasti dal prostý, dřevěný kříž. Jedná se o bíle natřený lidový kříž stojící v maličké ohrádce v horském uzlu Zadních hor na rozhraní Moravy a Slovenska. V roce 1986 bylo na Bílém kříži Ústavem ekologie krajiny Akademie věd ČR založeno Experimentální ekologické pracoviště. Probíhá zde výzkum vlivu zvýšené koncentrace oxidu uhličitého na lesy.</p>
<p>Vědecké pracoviště je možné po předchozí domluvě navštívit a vyslechnout si odborný výklad.</p>

<h3>Sulov</h3>

<p>V bezprostřední blízkosti Bílého kříže se nacházejí vrcholy Sulov (943 m n. m.) a Súl’ov (903 m n. m.), které ze severu resp. z jihu ohraničují prostory rekreační oblasti. Na úbočí Súlova se nachází výrazně vyvinutý mrazový srub. Řada turistických značek vede v létě turisty a cyklisty na všechny strany, v zimě pak lyžařům skýtá dobře sjízdné hřebenovky a upravené běžecké tratě na Gruň a Bílou.</p>

<h3>Gruň</h3>

<ul>
	<li>rozsáhlý horský hřeben, Beskydy</li>
	<li>vzdálenost cca 8 km od naší chalupy</li>
</ul>

<p>Gruň je rozsáhlý horský hřeben v části Beskyd nazývaných Zadní hory. Na hřebeni se mezi vrcholy Janikula (833 m n. m.) a Kozlena (886 m n. m.) rozkládá osada Gruň. Název Gruň pochází ze stejnojmenného valašského a lašského slova označujícího horský hřeben odlesněný pro potřeby salašnictví a přeměněný na rozsáhlé pastviny, tzv. valašské louky. Dodnes se po celé délce hřbetu zachovaly rozptýlené dřevěné chalupy, původně salaše. Některé salaše byly přeměněny na turistické chaty (Švarná Hanka, Charbulákov). Roku 1773 zde místní rolník nechal postavit sochu Panny Marie a později byla nad ní vybudována jednoduchá kaple. Ta byla koncem 19. století přestavena na dřevěný kostelík s hranolovou věží. </p>

<h3>Přírodní památka Obidová</h3>

<ul>
	<li>710–750 m n. m., Beskydy</li>
	<li>vzdálenost cca 4 km od naší chalupy</li>
</ul>

<p>Jedná se o nejcennější beskydské rašelinné louky (sihly) s řadou vzácných, ohrožených a zvláště chráněných druhů rostlin a živočichů. Nachází se poblíž obce Krásná v mělké podhřebenové sníženině v oblasti Sihelského potoka - jednoho z přítoků řeky Mohelnice.</p>

<h3>Tis na Byčinci</h3>

<p>Zajímavým lákadlem může být také 200 – 300 let starý památný strom na Byčinci. Tento tis červený zde dosahuje výšky téměř 8 metrů a jeho obvod se blíží ke 2 metrům.</p>

<h3>Naučná stezka Prameny Morávky</h3>

<p>Celková délka stezky je cca 14 km a najdete na ní 9 naučných tabulí informujících o historii budování přehrady Morávka, o řece Morávce a jejím prameni a historii celého regionu. Začíná na parkovišti Morávka – Lipový. Cíl naučné stezky je ve vzdálenosti 2 km od naší chalupy</p>

<h3>Slavíč</h3>

<ul>
	<li>1055 m n. m., Beskydy</li>
	<li>vzdálenost cca 11 km od naší chalupy</li>
</ul>

<p>Asi 5 km dlouhý hřbet Beskyd, zvedající se nad vodní nádrží Morávka a končící u chaty Slavíč, kde k severu pokračuje systémem hřbetů Ropice. Vrchol je porostlý smrčinou, na jižních svazích paseky s částečnými výhledy k Zadním horám. Na vrcholu je geodetický bod. </p>

<h3>Hrubá jedle v Rykalim</h3>

<p>Tato jedle je stará asi 250 let a je tak velká, že na její obejmutí je potřeba několika lidí. Nachází se v údolí pod vrchem Vysoký Rykali (1033 m n. m.).</p>

<h3>Památník vánočního stromu pro Vatikán</h3>

<p>V údolí Lúčky, asi 2 km od našeho ubytování na Visalajích, můžete najít místo, odkud byl v r. 1999 vytěžen vánoční smrk pro Vatikán. Místo něj je zde umístěna pamětní deska. </p>

<h2>Pěší turistika</h2>

<p>Okolí chalupy je atraktivní lokalitou pro všechny vášnivé turisty, ale také například pro rodiny s dětmi, kteří se chtějí vydat na hezký a třeba i nenáročný výšlap do hezkého prostředí pohoří Beskyd. Je zde obrovské množství značených turistických tras a řada zajímavých památek a míst (viz. výše), které mohou být lákavým cílem nebo příjemným spestřením Vaší cesty.</p>

<strong>Některé z turistických tras:</strong>

